        $products = DB::table("products")
            ->join('categories','products.category_id', '=','categories.id')
            ->selectRaw("
            products.*,
            categories.title as category_title
            ")
            ->where('branch_id', 1)
            ->get();

        $branches = Branch::all();
        return view('branch1.index', compact('products', 'branches'));

    }

